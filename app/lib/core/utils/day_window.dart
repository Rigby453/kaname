// Утилиты «оконных» вычислений календарного дня в ЛОКАЛЬНОМ времени.
//
// Почему local, а не UTC: поле scheduledAt хранится Drift как абсолютный момент,
// но читается обратно как ЛОКАЛЬНЫЙ DateTime (дефолтный режим Drift, без
// storeDateTimeAsText). Планировщик оперирует «настенным» временем пользователя:
// задача на 23:00 должна попадать в свой календарный день по местному времени.
// Если строить границы дня через DateTime.utc(...), то на Europe/Moscow (UTC+3)
// окна сдвигаются на 3 часа — задачи у полуночи уезжают в соседний день, а
// точка-индикатор в календаре не совпадает со списком. Поэтому все границы
// дня/недели/месяца и ключи индикаторов считаем по ЛОКАЛЬНЫМ компонентам.

/// Начало календарного дня [d] (локальная полночь 00:00).
DateTime localDayStart(DateTime d) => DateTime(d.year, d.month, d.day);

/// Конец календарного дня [d] = начало следующего дня (полуоткрытый интервал).
DateTime localDayEnd(DateTime d) => localDayStart(d).add(const Duration(days: 1));

/// Ключ дня по ЛОКАЛЬНЫМ компонентам (без toUtc) — для бакетинга «день с
/// задачами» в календарных сетках. Формат 'yyyy-m-d' (без ведущих нулей).
String localDayKey(DateTime d) => '${d.year}-${d.month}-${d.day}';

/// true, если [a] и [b] — один и тот же календарный день (по локальным Y/M/D).
bool isSameLocalDay(DateTime a, DateTime b) =>
    a.year == b.year && a.month == b.month && a.day == b.day;
